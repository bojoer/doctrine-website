<h2>Get Involved</h2>

<p>
  Want to get involved with the Doctrine community? This page aims to document
  the different ways you can get involved with the community to help with offering
  support or for getting support yourself.
</p>

<h3>Mailing Lists</h3>

<p>
  The primary means for communication between Doctrine users and developers are the
  mailing lists hosted with <a href="http://groups.google.com/" target="_BLANK">Google Groups</a>:
</p>

<ul>
  <li><a href="http://groups.google.com/group/doctrine-user" target="_BLANK">Doctrine User Mailing List</a></li>
  <li><a href="http://groups.google.com/group/doctrine-dev" target="_BLANK">Doctrine Developers Mailing List</a></li>
</ul>

<h3>IRC</h3>

<p>
 If you prefer a more intimate and instant way of communicating with other Doctrine
 users and developers you can use IRC. We host our channels on the <a href="http://freenode.net/" target="_BLANK">Freenode</a> network:
</p>

<ul>
  <li><a href="irc://irc.freenode.net/doctrine">irc.freenode.net/doctrine</a></li>
  <li><a href="irc://irc.freenode.net/doctrine-dev">irc.freenode.net/doctrine-dev</a></li>
  <li><a href="irc://irc.freenode.net/doctrine">irc.freenode.net/doctrine-mongodb-odm</a></li>
</ul>